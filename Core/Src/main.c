/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  uint8_t buff[255];
  uint8_t uid[MIFARE_UID_MAX_LENGTH];
  int32_t uid_len = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_TIM3_Init();
  MX_SPI1_Init();
  MX_TIM4_Init();
  MX_TIM11_Init();
  /* USER CODE BEGIN 2 */
  printf("testModbus\r\n");
  set_slaveID();
  set_led(LED_STATUS, LED_HEART);

  // Power
  discreteInputs[0] = false;

  // RFID
  inputRegisters[0] = 0;
  inputRegisters[1] = 0;
  // Parts
  inputRegisters[2] = 0;
  // Errors
  inputRegisters[3] = 0;
 
  modbus_init();

  PN532 pn532;
  PN532_SPI_Init(&pn532);
  if (PN532_GetFirmwareVersion(&pn532, buff) == PN532_STATUS_OK) {
    printf("Found PN532 with firmware version: %d.%d\r\n", buff[1], buff[2]);
  } else {
    return -1;
  }
  PN532_SamConfiguration(&pn532);
  printf("Waiting for RFID/NFC card...\r\n");  
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    static uint32_t last = 0;
    if (HAL_GetTick() - last > 1000) {
      last = HAL_GetTick();

      // 134ms no card - 75ms w/ card
      uid_len = PN532_ReadPassiveTarget(&pn532, uid, PN532_MIFARE_ISO14443A, 100);
      if (uid_len != PN532_STATUS_ERROR) {
        printf("Found card with UID: ");
        for (uint8_t i = 0; i < uid_len; i++) {
          printf("%02x ", uid[i]);
        }
        inputRegisters[0] = (uid[0]<<8) + uid[1];
        inputRegisters[1] = (uid[2]<<8) + uid[3];
        inputRegisters[2] = 0;
        inputRegisters[3] = 0;
        printf("\r\n");
        // start_sound();
      }
    }

    dashLoop();
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

